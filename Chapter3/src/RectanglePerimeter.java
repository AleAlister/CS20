public class RectanglePerimeter {
	public static void main(String[] args) {
		
		
		int width = 4;
		int length = 13;
		int perimeter;
		
		perimeter = 2 * length + 2 * width;
		
		System.out.println("The perimeter of this rectangle is: " + perimeter);
	
	}

}

